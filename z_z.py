@bot.message_handler(content_types=['text'])
def get_text_messages(message):
    if message.text.lower() == 'привет':
        bot.send_message(message.from_user.id,
                         'Привет 😊 я бот знаков зодиака, помогу тебе узнать твои сильные и слабые стороны.')
        markup_inline = types.InlineKeyboardMarkup()
        item_yes = types.InlineKeyboardButton(text='Да', callback_data='yes')
        item_no = types.InlineKeyboardButton(text='Нет', callback_data='no')
        markup_inline.add(item_yes, item_no)
        bot.send_message(message.chat.id, text='Желаешь ли ты узнать небольшую информацию?', reply_markup=markup_inline)
    elif message.text == '/help':
        bot.send_message(message.from_user.id, 'Напиши привет')
    elif message.text == '/start':
        bot.send_message(message.from_user.id, 'Рад тебя видеть 👋, напиши Привет')
    elif message.text == 'Овен ♈' or 'Телец ♉' or 'Близнецы ♊' or 'Рак ♋' or 'Лев  ♌' or 'Дева ♍' \
            or 'Весы ♎' or 'Скорпион ♏' or 'Стрелец ♐' or 'Козерог ♑' or 'Водолей ♒' or 'Рыбы ♓':
        for k, v in zodiaс_sign.items():
            if k == message.text:
                bot.send_message(message.from_user.id, f'{v}')
    else:
        bot.send_message(message.from_user.id, "Я тебя не понимаю. Напиши /help.")
    bot.answer_callback_query(callback_query_id=message.id)


